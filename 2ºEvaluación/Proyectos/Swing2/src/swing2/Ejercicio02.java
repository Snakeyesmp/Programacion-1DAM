    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        JRadioButton[] rdbs = {rbtnSumatorio, rbtnProducto, rbtnExponencial};

        int num1, num2, resultado = 0;

        for (int i = 0; i < rdbs.length; i++) {

            //Si esta seleccionado, coge el texto
            if (rdbs[0].isSelected()) {
                num1 = Integer.parseInt(txtNum1.getText());
                num2 = Integer.parseInt(txtNum2.getText());
                resultado = num1 + num2;

            } else if (rdbs[1].isSelected()) {
                num1 = Integer.parseInt(txtNum1.getText());
                num2 = Integer.parseInt(txtNum2.getText());
                resultado = num1 * num2;

            } else if (rdbs[2].isSelected()) {
                num1 = Integer.parseInt(txtNum1.getText());
                num2 = Integer.parseInt(txtNum2.getText());
                resultado = (int) Math.pow(num1, num2);

            }

        }
        JOptionPane.showMessageDialog(this, "Tu resultado es " + resultado);
    }//GEN-LAST:event_btnCalcularActionPerformed

