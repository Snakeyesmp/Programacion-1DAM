public class Ejercicio06 {
  
     public static void main(String[] args) {

        System.out.println("Desde 320 hasta 160 con Do While");

        int i;

        i = 320;

        do {
            
            System.out.println(i);
            i -= 20;

        } while (i >= 160);
    }  
}
